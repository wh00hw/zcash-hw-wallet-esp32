def crc16(data: bytes) -> int:
    crc = 0xFFFF
    for b in data:
        crc = ((crc << 8) ^ CRC16_TAB[((crc >> 8) ^ b) & 0xFF]) & 0xFFFF
    return crc

def encode(seq: int, msg_type: int, payload: bytes = b"") -> bytes:
    raw = bytes([HWP_MAGIC, HWP_VERSION, seq, msg_type]) + struct.pack("<H", len(payload)) + payload
    return raw + struct.pack("<H", crc16(raw))

def recv_frame(ser, timeout=10):
    start = time.time()
    buf = b""
    while time.time() - start < timeout:
        b = ser.read(1)
        if not b:
            continue
        buf += b
        idx = buf.find(bytes([HWP_MAGIC]))
        if idx >= 0 and len(buf) >= idx + 8:
            frame = buf[idx:]
            plen = struct.unpack_from("<H", frame, 4)[0]
            total = 6 + plen + 2
            if len(frame) >= total:
                return frame[:total]
    return None

def main():
    port = sys.argv[1] if len(sys.argv) > 1 else "/dev/ttyACM0"
    ser = serial.Serial(port, 115200, timeout=1)
    time.sleep(1)

    # Drain boot log
    d = ser.read(4096)
    if d:
        print(d.decode(errors="replace").strip())
        print()

    seq = 0

    # --- PING ---
    seq += 1
    print(f"[{seq}] TX PING")
    ser.write(encode(seq, MSG_PING))
    ser.flush()
    frame = recv_frame(ser)
    if frame and frame[3] == MSG_PONG:
        print(f"[{seq}] RX PONG — OK")
    else:
        print(f"[{seq}] FAILED: {frame.hex() if frame else 'timeout'}")
        ser.close()
        return

    # --- FVK_REQ ---
    seq += 1
    print(f"\n[{seq}] TX FVK_REQ")
    ser.write(encode(seq, MSG_FVK_REQ))
    ser.flush()
    frame = recv_frame(ser, timeout=30)
    if frame and frame[3] == MSG_FVK_RSP:
        payload = frame[6:6+96]
        ak   = payload[0:32]
        nk   = payload[32:64]
        rivk = payload[64:96]
        print(f"[{seq}] RX FVK_RSP — OK")
        print(f"  ak:   {ak.hex()}")
        print(f"  nk:   {nk.hex()}")
        print(f"  rivk: {rivk.hex()}")
    elif frame and frame[3] == MSG_ERROR:
        code = frame[6]
        msg = frame[7:6+struct.unpack_from("<H", frame, 4)[0]].decode(errors="replace")
        print(f"[{seq}] ERROR code={code}: {msg}")
    else:
        print(f"[{seq}] FAILED: {frame.hex() if frame else 'timeout'}")

    print("\nAll tests done.")
    ser.close()

if __name__ == "__main__":
    main()
