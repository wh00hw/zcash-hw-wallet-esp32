def crc16(data: bytes) -> int:
    crc = 0xFFFF
    for b in data:
        crc = ((crc << 8) ^ CRC16_TAB[((crc >> 8) ^ b) & 0xFF]) & 0xFFFF
    return crc

def encode_frame(seq: int, msg_type: int, payload: bytes = b"") -> bytes:
    raw = bytes([HWP_MAGIC, HWP_VERSION, seq, msg_type]) + struct.pack("<H", len(payload)) + payload
    c = crc16(raw)
    return raw + struct.pack("<H", c)

def main():
    port = sys.argv[1] if len(sys.argv) > 1 else "/dev/ttyACM0"
    print(f"Opening {port}...")

    ser = serial.Serial(port, 115200, timeout=3)
    time.sleep(0.5)

    # Drain boot log
    pending = ser.read(ser.in_waiting or 1)
    if pending:
        print(f"Boot log:\n{pending.decode('utf-8', errors='replace')}")

    ser.reset_input_buffer()
    time.sleep(0.1)

    ping_frame = encode_frame(seq=0x01, msg_type=MSG_PING)
    print(f"TX PING: {ping_frame.hex()}")
    ser.write(ping_frame)
    ser.flush()

    print("Waiting for response...")
    start = time.time()
    buf = b""
    while time.time() - start < 5:
        b = ser.read(1)
        if not b:
            continue
        buf += b
        idx = buf.find(bytes([HWP_MAGIC]))
        if idx >= 0 and len(buf) >= idx + 8:
            frame = buf[idx:]
            mtype = frame[3]
            plen = struct.unpack_from("<H", frame, 4)[0]
            total = 6 + plen + 2
            if len(frame) >= total:
                print(f"RX: {frame[:total].hex()}")
                print(f"  type=0x{mtype:02x} seq={frame[2]} payload_len={plen}")
                if mtype == MSG_PONG:
                    print("SUCCESS: PONG!")
                elif mtype == 0x04:
                    print("  FVK response")
                elif mtype == 0x07:
                    code = frame[6]
                    msg = frame[7:6+plen].decode("utf-8", errors="replace")
                    print(f"  ERROR code={code}: {msg}")
                ser.close()
                return

    print(f"Timeout. Got {len(buf)} bytes: {buf.hex()}")
    ser.close()

if __name__ == "__main__":
    main()
